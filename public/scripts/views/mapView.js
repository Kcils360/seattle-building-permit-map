function loadMarkers(data) {
  for (var i = 0; i < data.length; i++) {
    var coords = data[i].location.coordinates;
    var latLng = new google.maps.LatLng(coords[1],coords[0]);
    var marker = new google.maps.Marker({
      position: latLng,
      map: map
    });
  }
}
